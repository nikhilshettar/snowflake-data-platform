USE ROLE ACCOUNTADMIN;
USE WAREHOUSE INGEST_WH_XS;
CREATE OR REPLACE DATABASE DATA_PLATFORM;
USE DATABASE DATA_PLATFORM;
CREATE OR REPLACE SCHEMA LANDING;
USE SCHEMA LANDING;


--creating External Stage
CREATE OR REPLACE STORAGE INTEGRATION S3_STORAGE_INT
TYPE = EXTERNAL_STAGE
STORAGE_PROVIDER = S3
ENABLED = TRUE
STORAGE_AWS_ROLE_ARN = 'arn:aws:iam::260621207295:role/snowflake_s3_ingest_role'
STORAGE_ALLOWED_LOCATIONS = ('s3://company-data-landing-s3/landing/');


--File Format Creation
CREATE OR REPLACE FILE FORMAT CSV_FILE_FORMAT_STD
TYPE = 'CSV'
FIELD_DELIMITER = ','
SKIP_HEADER = 1
FIELD_OPTIONALLY_ENCLOSED_BY = '"'
NULL_IF = ('NULL','null');

--stage creation
CREATE OR REPLACE STAGE S3_LANDING_STAGE
URL = 's3://company-data-landing-s3/landing/'
STORAGE_INTEGRATION = S3_STORAGE_INT
FILE_FORMAT = CSV_FILE_FORMAT_STD;


DESC INTEGRATION S3_STORAGE_INT;

LIST @S3_LANDING_STAGE;

--Created Landing Table
CREATE OR REPLACE TABLE CUSTOMER_LANDING (
    CUSTOMER_ID NUMBER,
    FIRST_NAME STRING,
    LAST_NAME STRING,
    EMAIL STRING,
    COUNTRY STRING,
    CREATED_DATE DATE,
    FILE_NAME STRING,
    LOAD_TS TIMESTAMP DEFAULT CURRENT_TIMESTAMP()
);

--Created Final Table 
CREATE OR REPLACE TABLE CUSTOMER_FINAL (
    CUSTOMER_ID NUMBER,
    FIRST_NAME STRING,
    LAST_NAME STRING,
    EMAIL STRING,
    COUNTRY STRING,
    CREATED_DATE DATE,
    LOAD_TS TIMESTAMP
);


--Created table to load Rejected Records
CREATE TABLE IF NOT EXISTS CUSTOMER_REJECTED (
    CUSTOMER_ID STRING,
    FIRST_NAME STRING,
    LAST_NAME STRING,
    EMAIL STRING,
    COUNTRY STRING,
    CREATED_DATE STRING,
    ERROR_MESSAGE STRING,
    FILE_NAME STRING,
    LOAD_TS TIMESTAMP DEFAULT CURRENT_TIMESTAMP()
);


--Created Audit Table 
CREATE OR REPLACE TABLE INGEST_AUDIT_LOG (
    FILE_NAME STRING,
    ROWS_PARSED NUMBER,
    ROWS_LOADED NUMBER,
    ROWS_REJECTED NUMBER,
    STATUS STRING,
    LOAD_TS TIMESTAMP DEFAULT CURRENT_TIMESTAMP()
);

--Created stream for continuous ingestion
CREATE OR REPLACE STREAM CUSTOMER_LANDING_STREAM
ON TABLE CUSTOMER_LANDING
APPEND_ONLY = TRUE;


--bulk load using copy command
COPY INTO CUSTOMER_LANDING
FROM (
    SELECT
        TRY_TO_NUMBER($1),
        $2,
        $3,
        $4,
        $5,
        TRY_TO_DATE($6),
        METADATA$FILENAME,
        CURRENT_TIMESTAMP()
    FROM @S3_LANDING_STAGE/customer/
)
FILE_FORMAT = (FORMAT_NAME = CSV_FILE_FORMAT_STD)
ON_ERROR = CONTINUE;



