---create snowpipe using autoingest

CREATE OR REPLACE PIPE CUSTOMER_AUTO_PIPE
AUTO_INGEST = TRUE
NOTIFICATION_INTEGRATION = SQS_NOTIFICATION_INT
AS
COPY INTO CUSTOMER_LANDING
FROM (
    SELECT
        TRY_TO_NUMBER($1),
        $2,
        $3,
        $4,
        $5,
        TRY_TO_DATE($6),
        METADATA$FILENAME,
        CURRENT_TIMESTAMP()
    FROM @S3_LANDING_STAGE/customer/
)
FILE_FORMAT = (FORMAT_NAME = CSV_FILE_FORMAT_STD)
ON_ERROR = CONTINUE;

---validate the pipeline status

SELECT SYSTEM$PIPE_STATUS('CUSTOMER_AUTO_PIPE');

--create processing task to move data from landing to main table

CREATE OR REPLACE TASK TASK_PROCESS_CUSTOMER
WAREHOUSE = INGEST_WH_XS
SCHEDULE = '1 MINUTE'
WHEN SYSTEM$STREAM_HAS_DATA('CUSTOMER_LANDING_STREAM')
AS
BEGIN


    INSERT INTO CUSTOMER_REJECTED
    SELECT
        CUSTOMER_ID,
        FIRST_NAME,
        LAST_NAME,
        EMAIL,
        COUNTRY,
        CREATED_DATE,
        'INVALID DATA',
        FILE_NAME,
        CURRENT_TIMESTAMP()
    FROM CUSTOMER_LANDING_STREAM
    WHERE CUSTOMER_ID IS NULL
       OR CREATED_DATE IS NULL;


    CREATE OR REPLACE TEMP TABLE TMP_VALID AS
    SELECT *
    FROM (
        SELECT *,
               ROW_NUMBER() OVER (
                   PARTITION BY CUSTOMER_ID
                   ORDER BY LOAD_TS DESC
               ) RN
        FROM CUSTOMER_LANDING_STREAM
        WHERE CUSTOMER_ID IS NOT NULL
          AND CREATED_DATE IS NOT NULL
    )
    WHERE RN = 1;


    MERGE INTO CUSTOMER_FINAL T
    USING TMP_VALID S
    ON T.CUSTOMER_ID = S.CUSTOMER_ID

    WHEN MATCHED THEN UPDATE SET
        T.FIRST_NAME = S.FIRST_NAME,
        T.LAST_NAME = S.LAST_NAME,
        T.EMAIL = S.EMAIL,
        T.COUNTRY = S.COUNTRY,
        T.CREATED_DATE = S.CREATED_DATE,
        T.LOAD_TS = CURRENT_TIMESTAMP()

    WHEN NOT MATCHED THEN INSERT
    VALUES (
        S.CUSTOMER_ID,
        S.FIRST_NAME,
        S.LAST_NAME,
        S.EMAIL,
        S.COUNTRY,
        S.CREATED_DATE,
        CURRENT_TIMESTAMP()
    );


    INSERT INTO INGEST_AUDIT_LOG
    SELECT
        FILE_NAME,
        COUNT(*),
        SUM(CASE WHEN CUSTOMER_ID IS NOT NULL
                  AND CREATED_DATE IS NOT NULL THEN 1 ELSE 0 END),
        SUM(CASE WHEN CUSTOMER_ID IS NULL
                  OR CREATED_DATE IS NULL THEN 1 ELSE 0 END),
        'SUCCESS',
        CURRENT_TIMESTAMP()
    FROM CUSTOMER_LANDING_STREAM
    GROUP BY FILE_NAME;

END;

--enable the task
ALTER TASK TASK_PROCESS_CUSTOMER RESUME;


--validation for Full load
SELECT * FROM CUSTOMER_FINAL;
SELECT * FROM INGEST_AUDIT_LOG;

--validation incremental load
SELECT * FROM CUSTOMER_FINAL ORDER BY CUSTOMER_ID;

SELECT CUSTOMER_ID, COUNT(*)
FROM CUSTOMER_FINAL
GROUP BY CUSTOMER_ID
HAVING COUNT(*) > 1;

--validation error file
SELECT * FROM CUSTOMER_REJECTED;

