--Semantic Layer
CREATE OR REPLACE VIEW SEMANTIC.V_CUSTOMER_SUMMARY AS
SELECT
    REGION,
    CUSTOMER_TYPE,
    COUNT(*) AS TOTAL_CUSTOMERS
FROM TRANSFORMATION.CUSTOMER_TRANSFORMED
GROUP BY REGION, CUSTOMER_TYPE;

--created view on top of semantic layer
CREATE OR REPLACE VIEW SEMANTIC.V_CUSTOMER_DETAILS AS
SELECT
    CUSTOMER_ID,
    FIRST_NAME || ' ' || LAST_NAME AS FULL_NAME,
    EMAIL,
    REGION,
    CUSTOMER_TYPE,
    CREATED_DATE
FROM TRANSFORMATION.CUSTOMER_TRANSFORMED;


